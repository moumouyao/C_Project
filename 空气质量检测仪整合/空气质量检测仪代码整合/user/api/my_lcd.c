	void show_data2LCD(void){
		char str[64] = {0};
		sprintf(str, "%.2f", (float)kqm_data.CO2);	//二氧化碳
		LCD_ShowString(96, 32, strlen(str) * 8, 16, 16, (uint8_t*)str);
		
		sprintf(str, "%.2f", (float)ADC1_value[0] / 4095);	//烟雾浓度
		LCD_ShowString(96, 64, strlen(str) * 8, 16, 16, (uint8_t*)str);
		
		sprintf(str, "%.2f", (float)kqm_data.CHO);	//甲烷
		LCD_ShowString(96, 96, strlen(str) * 8, 16, 16, (uint8_t*)str);
		
		if(dht_data.tmp_flag == 0){	//温度为正
				sprintf(str, "%d.%d", dht_data.tmp, dht_data.tmp_flo);	//温度
				}
				else{
				sprintf(str, "-%d.%d", dht_data.tmp, dht_data.tmp_flo);	//温度
				}
		
		LCD_ShowString(96, 128, strlen(str) * 8, 16, 16, (uint8_t*)str);
		
		sprintf(str, "%d", dht_data.hum);	//湿度
		LCD_ShowString(96, 160, strlen(str) * 8, 16, 16, (uint8_t*)str);
				//printf("%d.%d", dht_data.tmp, dht_data.tmp_flo);
		
	}
	
	
